package Mathematics;

/**
 * Interface declaring a method of getting and setting the count of a class.
 * @author Rune Dahl Iversen
 */
public interface Count {
    /**
     * Gets the count of this instance.
     * @return The count of this instance.
     */
    public int getCount();

    /**
     * Sets the count of this instance.
     * @param count Count to be set.
     */
    public void setCount(int count);
}